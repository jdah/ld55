v4 palette_get(int i) {
    ASSERT(i >= 0 && i < 64);
    const u32 c = PALETTE[i];
    return v4_of(
        ((c >> 16) & 0xFF) / 255.0f,
        ((c >>  8) & 0xFF) / 255.0f,
        ((c >>  0) & 0xFF) / 255.0f,
        1.0f);
}

